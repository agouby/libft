#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n > 0)
	{
		if (n > 7)
		{
			n -= 8;
			*((long *)(s + n)) = 0;
		}
		else if (n > 3)
		{
			n -= 4;
			*((int *)(s + n)) = 0;
		}
		else
		{
			n--;
			*((char *)(s + n)) = 0;
		}
	}
}
