#include "libft.h"

void	ft_tabdel(char ***tab)
{
	int i;

	i = 0;
	if (!*tab || !**tab)
		return ;
	while ((*tab)[i])
	{
		ft_strdel(&(*tab)[i]);
		i++;
	}
	ft_memdel((void **)&(*tab));
}
